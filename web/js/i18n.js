/* --------------------------------------------------------------------------
   i18n Functions
   -------------------------------------------------------------------------- */
let currentLanguage = localStorage.getItem('language') || 'ar';

function t(key) {
  const lang = currentLanguage;
  const value = translations[lang] && translations[lang][key];
  if (value !== undefined) return value;

  const fallback = lang === 'ar' ? 'en' : 'ar';
  const fallbackValue = translations[fallback] && translations[fallback][key];
  if (fallbackValue !== undefined) return fallbackValue;

  return key;
}

function setLanguage(lang) {
  if (lang !== 'ar' && lang !== 'en') return;

  currentLanguage = lang;
  localStorage.setItem('language', lang);

  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translated = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = translated;
    } else {
      el.textContent = translated;
    }
  });

  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    el.title = t(key);
  });

  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria');
    el.setAttribute('aria-label', t(key));
  });

  document.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
}

function getLanguage() {
  return currentLanguage;
}

function toggleLanguage() {
  const newLang = currentLanguage === 'ar' ? 'en' : 'ar';
  setLanguage(newLang);
  return newLang;
}

function initLanguage() {
  const saved = localStorage.getItem('language');
  if (saved && (saved === 'ar' || saved === 'en')) {
    currentLanguage = saved;
  } else {
    const browserLang = navigator.language || navigator.userLanguage;
    currentLanguage = browserLang && browserLang.startsWith('ar') ? 'ar' : 'ar';
  }

  document.documentElement.lang = currentLanguage;
  document.documentElement.dir = currentLanguage === 'ar' ? 'rtl' : 'ltr';

  const langBtns = document.querySelectorAll('.navbar-lang-btn');
  langBtns.forEach(btn => {
    btn.textContent = currentLanguage === 'ar' ? 'EN' : 'عربي';
  });
}
